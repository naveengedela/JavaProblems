package thirtyDaysOfCode;

public class PrintOneHundred_14 {
    public static void main(String[] args) {
        int counter = 'A'/ 'A';
        String s = "..........";

        for (int i = counter; i <= s.length() * 10; i++) {
            System.out.println(i);
        }

        System.out.println("===========================");


        for (int i = counter; i <= 'd'; i++) {
            System.out.println(i);
        }
    }
}
